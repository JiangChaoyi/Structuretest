package com.npb.domian;

public class CompoundDetails {
	String NPB_No;
	String Product_name;
	String Structure;
	String Descrip;
	String Molecular_Formula;	
	String Molecular_Weight;	
	String InchiKey;	
	String Smiles;	
	String Cas_no;	
	String PubChem_ID;	
	String DocTypes;
	String Species;
	public String getNPB_No() {
		return NPB_No;
	}
	public void setNPB_No(String nPB_No) {
		NPB_No = nPB_No;
	}
	public String getProduct_name() {
		return Product_name;
	}
	public void setProduct_name(String product_name) {
		Product_name = product_name;
	}
	public String getStructure() {
		return Structure;
	}
	public void setStructure(String structure) {
		this.Structure = structure;
	}
	public String getDescrip() {
		return Descrip;
	}
	public void setDescrip(String descrip) {
		Descrip = descrip;
	}
	public String getMolecular_Formula() {
		return Molecular_Formula;
	}
	public void setMolecular_Formula(String molecular_Formula) {
		Molecular_Formula = molecular_Formula;
	}
	public String getMolecular_Weight() {
		return Molecular_Weight;
	}
	public void setMolecular_Weight(String molecular_Weight) {
		Molecular_Weight = molecular_Weight;
	}
	public String getInchiKey() {
		return InchiKey;
	}
	public void setInchiKey(String inchiKey) {
		InchiKey = inchiKey;
	}
	public String getSmiles() {
		return Smiles;
	}
	public void setSmiles(String smiles) {
		Smiles = smiles;
	}
	public String getCas_no() {
		return Cas_no;
	}
	public void setCas_no(String cas_no) {
		Cas_no = cas_no;
	}
	public String getPubChem_ID() {
		return PubChem_ID;
	}
	public void setPubChem_ID(String pubChem_ID) {
		PubChem_ID = pubChem_ID;
	}
	public String getDocTypes() {
		return DocTypes;
	}
	public void setDocTypes(String docTypes) {
		this.DocTypes = docTypes;
	}
	public String getSpecies() {
		return Species;
	}
	public void setSpecies(String species) {
		Species = species;
	}

}
